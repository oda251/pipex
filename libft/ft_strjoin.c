#include "libft.h"

char	*ft_strjoin(char const *s1, const char *s2)
{
	char	*dest;
	size_t	len1;
	size_t	len2;

	if (!s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	dest = malloc(sizeof(char) * (len1 + len2 + 1));
	if (!dest)
		return (NULL);
	ft_strcpy(dest, s1);
	ft_strcpy(dest + len1, s2);
	dest[len1 + len2] = '\0';
	return (dest);
}
